import matplotlib.pyplot as plt

# Створюємо словник даних
data = {
    "Країна": ["Україна", "Канада", "Єгипет", "Китай", "Бразилія", "Австралія", "Індія", "Південна Африка", "Франція", "Японія"],
    "Площа (км²)": [603628, 9984670, 1002450, 9596960, 8515767, 7692024, 3287263, 1221037, 551695, 377975],
    "Населення": [41723998, 38005238, 104124440, 1444216107, 213993437, 25687041, 1393409038, 59308690, 65273511, 125960000],
    "Континент": ["Європа", "Північна Америка", "Африка", "Азія", "Південна Америка", "Австралія", "Азія", "Африка", "Європа", "Азія"]
}

# Підраховуємо кількість країн в кожному континенті
continent_counts = {"Африка": 0, "Азія": 0, "Інші": 0}

for continent in data["Континент"]:
    if continent in continent_counts:
        continent_counts[continent] += 1
    else:
        continent_counts["Інші"] += 1

# Дані для кругової діаграми
labels = continent_counts.keys()
sizes = continent_counts.values()
colors = ['gold', 'yellowgreen', 'lightcoral']

# Побудова кругової діаграми
plt.pie(sizes, labels=labels, colors=colors, autopct='%1.1f%%', startangle=140)
plt.title('Відсоток країн за континентами')
plt.axis('equal')  # Забезпечуємо круглу форму діаграми

# Виведення діаграми на екран
plt.show()
