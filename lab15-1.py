import pandas as pd

# Створення початкового словника з іншими країнами
data = {
    "Країна": ["Норвегія", "Мексика", "В'єтнам", "Південна Африка", "Польща", "Чилі", "Індонезія", "Нова Зеландія"],
    "Населення (млн)": [5.4, 126.0, 98.0, 60.0, 37.8, 19.0, 276.0, 5.1],
    "Частина світу": ["Європа", "Північна Америка", "Азія", "Африка", "Європа", "Південна Америка", "Азія", "Океанія"]
}

# Перетворення словника на DataFrame
df = pd.DataFrame(data)

# Виведення початкового DataFrame
print("Початковий DataFrame:")
print(df)

# Додавання нових даних до DataFrame
new_data = {
    "Країна": ["Куба", "Південна Корея", "Кенія", "Колумбія"],
    "Населення (млн)": [11.3, 52.0, 55.0, 50.9],
    "Частина світу": ["Північна Америка", "Азія", "Африка", "Південна Америка"]
}

df = pd.concat([df, pd.DataFrame(new_data)], ignore_index=True)

print("\nDataFrame після доповнення даними:")
print(df)

# Агрегація: сумарне населення за частинами світу
grouped_data = df.groupby("Частина світу")["Населення (млн)"].sum().reset_index()

# Виведення результату
print("\nСумарне населення за частинами світу:")
print(grouped_data)

# Додаткова агрегація: середнє населення за частинами світу
average_population = df.groupby("Частина світу")["Населення (млн)"].mean().reset_index()
print("\nСереднє населення за частинами світу:")
print(average_population)
